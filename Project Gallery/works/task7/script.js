let x = 10;
console.log("X value: " + x);

let y = x + 5;
console.log("Addition of x and y: " + y);

let z = y - x;
console.log("Subtraction of y and x: " + z);

let p = x * z;
console.log("Multiplication of x and z: " + p);

let q = p / x;
console.log("Division of p and x: " + q);

let r = x ** q;
console.log("Modulo of x and q: " + r);